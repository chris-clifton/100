# Write a method that sorts an array using merge sort
# Merge sort breaks down array elements into nested subarrays, then recombines those nested sub-arrays in sorted order

# input: array
# output: sorted array
# rules: use merge sort
# algorithm: 
  # break array down into smaller subarrays, and recursively, break them down even further
  # call up the merge method we made in previous exercise, sort and merge final two arrays

def merge(array1, array2)
  index2 = 0
  result = []

  array1.each do |value|
    while index2 < array2.size && array2[index2] <= value
      result << array2[index2]
      index2 += 1
    end
    result << value
  end

  result.concat(array2[index2..-1])
end

def merge_sort(array)
  return array if array.size == 1

  sub_array_1 = array[0...array.size / 2]
  sub_array_2 = array[array.size / 2...array.size]

  sub_array_1 = merge_sort(sub_array_1)
  sub_array_2 = merge_sort(sub_array_2)

  merge(sub_array_1, sub_array_2)
end


